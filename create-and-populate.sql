# Initialize database 
create database hospitaldatabase;

# specify which database to use
use hospitaldatabase;

# Initialize tables 
CREATE TABLE Room (
Rnumber int NOT NULL,
RStatus varchar(8),
PRIMARY KEY (Rnumber));

CREATE TABLE Patient (
PID int NOT NULL,
PFName varchar(20) NOT NULL,
PLName varchar(20) NOT NULL,
PPhoneNo char(14), # format (999) 999-9999
PEmail varchar(45),
PDOB date NOT NULL,
PAddress varchar(100),
PSex char(1) NOT NULL,
PHeight varchar(3) NOT NULL, #height in inches
PWeight varchar(3) NOT NULL, #weight in pounds
RNumber int,
PRIMARY KEY (PID));

CREATE TABLE EmergencyContact (
CName varchar(40) NOT NULL,
PID int NOT NULL,
PatientRelation varchar(15) NOT NULL,
PhoneNo char(14) NOT NULL,
PRIMARY KEY (CName, PID));

CREATE TABLE Employee (
EID int NOT NULL,
EFNAME varchar(20) NOT NULL,
ELName varchar(20) NOT NULL,
ECredentials varchar(20) NOT NULL,
EPhoneNo char(14) NOT NULL,
EEmail varchar(45) NOT NULL,
EAddress varchar(100) NOT NULL,
EType char(1) NOT NULL,
PRIMARY KEY (EID));

CREATE TABLE Nurse (
NID int NOT NULL,
PRIMARY KEY (NID));

CREATE TABLE Doctor (
DID int NOT NULL,
DSpecialty varchar(25) NOT NULL,
OfficeNo int,
PRIMARY KEY (DID));

CREATE TABLE DoctorSupervision (
DID int NOT NULL,
NID int NOT NULL,
Department varchar(25),
PRIMARY KEY (DID, NID));

CREATE TABLE Visit (
VID int NOT NULL,
PID int NOT NULL,
DID int NOT NULL,
NID int,
DateIn date NOT NULL,
DateOut date,
PRIMARY KEY (VID));

CREATE TABLE ActionTaken (
VID int NOT NULL,
ActTaken varchar(200) NOT NULL,
PRIMARY KEY (VID, ActTaken));

